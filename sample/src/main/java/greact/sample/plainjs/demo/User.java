package greact.sample.plainjs.demo;

public class User {
    final public long id;
    final public String name;
    final public int age;
    final public String sex;

    public User(long id, String name, int age, String sex) {
        this.id = id;
        this.name = name;
        this.age = age;
        this.sex = sex;
    }
}
